SELECT dem.AFF_T_DISCO as AFFAIRE
, dem.DEM_ID_PRM as PRM
, nat.NAT_C_PRESTATION PRESTATION
, dem.DEM_R_STATUT STATUT, ee.EST_T_ETAT as ETAT_EXTERNE
, frn.FRN_T_ACTEUR as FOURNISSEUR
, TO_CHAR(dem.DEM_D_DEMANDE, 'DD/MM/YYYY') DATE_DEMANDE
, TO_CHAR(t_pres.PRE_DEM_DATEEFFET, 'DD/MM/YYYY') DATE_EFFET_SOUHAITEE
, TO_CHAR(t_rece.PRE_REC_REC_DATE_EFFET, 'DD/MM/YYYY') DATE_EFFET_PREVUE
, TO_CHAR(cr.CR_DATE, 'DD/MM/YYYY') DATE_REAL_CR
, TO_CHAR(realn.PRE_REA_R_DATEEFFET, 'DD/MM/YYYY') DATE_EFFET_REELLE
, TO_CHAR(dem.DEM_D_CLOTURE, 'DD/MM/YYYY') DATE_CLOTURE_SGE
, DECODE(dem.DEM_B_IS_GINKO, 'O', 'GINKO', 'N', 'COSY') SI
FROM SUIVI.DEMANDE dem
JOIN SUIVI.NATURE nat ON dem.DEM_K_NAT = nat.NAT_ID
JOIN SUIVI.FOURNISSEUR frn ON frn.FRN_ID = dem.DEM_K_FRN
LEFT JOIN SUIVI.ETAT_STATUT ee ON dem.DEM_K_EST_EXT = ee.EST_ID
LEFT JOIN SGEL_IAP_SCH.T_AFFAIRE aff ON dem.AFF_T_DISCO = aff.AFF_ID
LEFT JOIN SGEL_IAP_SCH.T_PRESTATION t_pres ON aff.AFF_ID_SEQUENCE = t_pres.AFF_ID_SEQUENCE
LEFT JOIN SGEL_IAP_SCH.T_RECEVABILITE t_rece ON t_pres.PRE_REC_ID = t_rece.PRE_REC_ID
LEFT JOIN SGEL_IAP_SCH.T_INTERVENTION_AFFAIRE ia ON dem.AFF_T_DISCO = ia.AFF_ID
LEFT JOIN SGEL_IAP_SCH.T_INTERVENTION t_inte ON ia.INT_ID = t_inte.INT_ID
LEFT JOIN SGEL_IAP_SCH.T_PLANIFICATION t_plan ON t_inte.PLA_ID =  t_plan.PLA_ID
LEFT JOIN SGEL_IAP_SCH.T_COMPTE_RENDU cr ON t_plan.CR_ID = cr.CR_ID
LEFT JOIN SGEL_IAP_SCH.T_REALISATION realn ON realn.PRE_REA_ID = t_pres.PRE_REA_ID
WHERE 1=1
AND t_pres.PRE_DEM_DATEEFFET = TO_DATE('01/01/2021')
 -- AND dem.AFF_T_DISCO = 'A056YUZB'
AND dem.AFF_T_DISCO IN @@IN1@@